# coding: utf-8

""" Υπολογισμός ν παραγοντικού -> 1 * 2 * 3 * ... * ν """

# στην αρχή το γινόμενο είναι 1
sum_n = 1
n = int(input("dwse n paragontiko: "))

# εδώ κάνουμε μια επανάληψη μέχρι να φτάσουμε στον αριθμό που έδωσε ο χρήστης 
# και σε κάθε επανάληψη πολλαπλασιάζουμε τον αριθμό με το γινόμενο των προηγούμενων αριθμών  
for i in range(1, n+1, 1):
    sum_n *= i

# τέλος εμφανίζουμε το αποτέλεσμα
print(sum_n)
